#include <stdio.h>
int main(void)
{
    // Float -%f
    float f = 54.33F;
    printf("Float - %f\n", f);

    // Double - %lf
    double d = 54673.99;
    printf("Double - %lf\n", d);

    // long double-%Lf
    long double ld = 386596.7568544;
    printf("Long double - %Lf\n", ld);

    return 0;
}